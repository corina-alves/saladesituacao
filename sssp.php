          <h2><strong>Origem da Sala de Situação</strong></h2>
          <p>
            A primeira tentativa de criar uma rede de <strong>alerta a inundações</strong>, em São Paulo, foi feita em
            fevereiro de 1976, como consequência de uma chuva excepcional, em fins de janeiro,
            que quase causou o transbordamento do <strong>Reservatório de Guarapiranga </strong>(o que <strong>não
              ocorreu</strong> devido à pronta <strong>intervenção do SP-Águas,</strong>
            que enviou ao local consultores, funcionários e caminhões com sacos de areia para altear e proteger a crista
            do reservatório).
            Os <strong>dados pluviométricos</strong> medidos em algumas estações do SP-Águas e da EMAE eram transmitidos,
            por meio de ligação telefônica, a um engenheiro do CTH, Dr. Sérgio Roberto Cirne de Toledo,
            que os organizava em uma tabela e os levava, pessoalmente, para o gabinete do Superintendente
          </p>
          <p>
            No fim da década de 1970, através de convênio entre o antigo DAEE, agora sendo SP-Águas e FDTE – Fundação para o Desenvolvimento
            Tecnológico de Engenharia da Escola Politécnica da USP, na época, foram implantadas as primeiras estações
            automáticas telemétricas com transmissão dos dados através de linhas telefônicas privativas. A Rede
            Telemétrica começou com cinco postos e foi se expandindo até atingir a configuração que temos hoje, com
            aproximadamente 200 postos, e estudos para a implementação de mais 100.
          </p>
